void PrintImage(int[,] image)
{
    for (int i = 0; i < image.GetLength(0); i++)//matrix.GetLength(0) вместо написания руками кол-ва строк
    {
        for (int j = 0; j < image.GetLength(1); j++)//matrix.GetLength(0) вместо написания руками кол-ва столбцов
        {
            //Console.Write($"{matr[i, j]} ");
            if (image[i, j] == 0)
                Console.Write($" ");
            else
                Console.Write($"+");
        }
        Console.WriteLine();
    }
}

